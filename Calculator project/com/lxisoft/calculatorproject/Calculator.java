package com.lxisoft.calculatorproject;
public class Calculator{
	
	public double addition(double result)
	{
		
		return +result;
	}
	   
	public double subtraction(double x ,double y,double z)
	{
		return x-y-z;
	}
	
	public double multiplication(double x ,double y,double z)
	{
		return x*y*z;
	}
	
	public double division(double x ,double y,double z)
	{
		return x/y/z;
	}
	
}